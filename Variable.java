class Variable {
    
    public static void main(String[] args) {
        
        int num1 = 12;
        int num2 = 24;
        int result = num1 + num2;
        
        // Displaying the sum of num1 and num2 suggessted by the prompt
        System.out.println("Sum of " + num1 + " and " + num2 + " is " + (num1 + num2));

        // Displaying the sum of num1 and num2 suggessted by the user
        System.out.println(num1 + num2);

        // Displaying the sum of num1 and num2 using a variable
        System.out.println(result);
    
    }
}